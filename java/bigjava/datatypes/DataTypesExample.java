package com.pawanchoure.bigjava.datatypes;

public class DataTypesExample {

	public static void main(String[] args) {
	System.out.println("Integer/Integer :" + 7/4);
	System.out.println("Integer/Float :" + 7/4.0);
	System.out.println("Float/Integer :" + 7.0/4);
	double doubleDivision=1+2+3/3;
	System.out.println("doubleDivision :" + doubleDivision) ;

	}

}
